class StackQueue
{
private:
    // These are STL stacks ( http://goo.gl/LxlRZQ )
    stack<int> s1;
    stack<int> s2;

public:
    void push(int);
    int pop();
};

//using 2 stacks
void StackQueue ::push(int x)
{
    // Your Code
    //s1 all elements into s2
    while (!s1.empty())
    {
        s2.push(s1.top());
        s1.pop();
    }
    //new element into s1
    s1.push(x);
    //again put into s1 from s2
    while (!s2.empty())
    {
        s1.push(s2.top());
        s2.pop();
    }
}

//Function to pop an element from queue by using 2 stacks.
int StackQueue ::pop()
{
    // Your Code
    //ans in s1
    int ans;
    if (!s1.empty())
    {
        ans = s1.top();
        s1.pop();
    }
    else
    {
        ans = -1;
    }
    return ans;
}
