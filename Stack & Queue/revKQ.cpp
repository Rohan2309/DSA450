queue<int> modifyQueue(queue<int> q, int k)
{
    //add code here.
    stack<int> s;
    //q has 1 2 3 4 5
    for (int i = 1; i <= k; i++)
    {
        s.push(q.front()); //pushing k eles in stk
        q.pop();
    }
    //q has 4 5
    //s has 1 2 3
    while (!s.empty())
    {
        q.push(s.top());
        s.pop();
    }
    //q has 4 5 3 2 1 (as we case push from back only)

    int n = q.size(); // n is 5 and k is 3
    int m = n - k;    //as we have to insert only 4 and 5 which are 2 elements (5-3)
    for (int i = 1; i <= m; i++)
    {
        int x = q.front();
        q.pop();
        q.push(x);
    }
    //q is 3 2 1 4 5
    return q;
}
