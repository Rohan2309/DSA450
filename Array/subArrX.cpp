int smallestSubWithSum(int arr[], int n, int x)
{
    // Your code goes here
    int mi = INT_MAX; //length of smallest sub array
    int i, j;         //iterators
    int s = 0;        //sum of curr sub array
    int start, end;
    start = end = 0; //pointers
    while (end < n)
    {
        while (s <= x and end < n)
        {
            s += arr[end++];
        }
        //we got a sub array till now
        while (s > x and start < n)
        {
            if (end - start < mi)
            {
                mi = end - start; //got sub array
            }
            s -= arr[start++]; //now decreasing from the start to check if a smaller sub array forms
        }
    }
    return mi;
}
